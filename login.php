    $stmt->execute();
    $result = $stmt->get_result();

    if ($result->num_rows > 0) {
        $row = $result->fetch_assoc();
        $_SESSION['idmember'] = $row['idmember'];
        $_SESSION['user'] = $username;
        $_SESSION['role'] = $row['profile']; 

